    private void Btn_ingresarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Btn_ingresarMouseClicked
        if (prcs_repetidos.isNoneEmpty(Txt_buscar1, Txt_buscar2, Txt_buscar3, Txt_buscar4)) {
            TrasladoProductoDAO pisosdao = new TrasladoProductoDAO();
            LocalDateTime locaDate = LocalDateTime.now();
            int anio = locaDate.getYear();
            int mes = locaDate.getMonthValue();
            int dia = locaDate.getDayOfMonth();
            String fechaOrden = String.valueOf(anio + "-" + mes + "-" + dia);
            
            int horas = locaDate.getHour();
            int minutos = locaDate.getMinute();
            int segundos = locaDate.getSecond();
            String horaOrden = String.valueOf(horas + ":" + minutos + ":" + segundos);

            pisos.setCodigoProducto(Txt_buscar4.getText());
            pisos.setCodigoBodega(Txt_buscar1.getText());
            pisos.setCantidadProducto(Txt_buscar3.getText());
            pisos.setFechaSalida(fechaOrden);
            pisos.setHoraSalida(horaOrden);
            pisos.setEstado("1");
            pisos.setBodegaDestino(Txt_buscar2.getText());
            pisosdao.insert(pisos);
            actualizarTabla("");
            prcs_repetidos.AlertaMensaje("guardado", "Traslado", "exitosamente");
            Limpiar();
            bitacora.GuardarEnBitacora("Guardar", "2004");
        } else {
        }
    }//GEN-LAST:event_Btn_ingresarMouseClicked

