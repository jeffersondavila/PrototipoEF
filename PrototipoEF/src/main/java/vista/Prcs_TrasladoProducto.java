    public void actualizarTabla(String codigo) {
        ProcesosRepetidos prcs_repetidos = new ProcesosRepetidos();
        TrasladoProductoDAO.codigoAuxiliar = codigo;
        TrasladoProductoDAO.nombreAuxiliar = codigo;
        String columnas[] = {"ID", "PRODUCTO", "BODEGA", "CANTIDAD", "FECHA", "HORA", "DESTINO", "ESTADO"};
        int cantidadcolumnas = columnas.length;
        prcs_repetidos.llenarColumnas(columnas, cantidadcolumnas, Tbl_Datos);
        String datos[] = new String[cantidadcolumnas];
        int tamaño[] = {75, 75, 75, 100, 75, 100, 100, 75};
        TrasladoProductoDAO pisosdao = new TrasladoProductoDAO();
        List<TrasladoProducto> piso = pisosdao.select();
        for (TrasladoProducto listaServicio : piso) {
            datos[0] = String.valueOf(listaServicio.getCodigo());
            datos[1] = String.valueOf(listaServicio.getCodigoProducto());
            datos[2] = String.valueOf(listaServicio.getCodigoBodega());
            datos[3] = String.valueOf(listaServicio.getCantidadProducto());
            datos[4] = String.valueOf(listaServicio.getFechaSalida());
            datos[5] = String.valueOf(listaServicio.getHoraSalida());
            datos[6] = String.valueOf(listaServicio.getBodegaDestino());
            if (String.valueOf(listaServicio.getEstado()).equals("1")) {
                datos[7] = "Activo";
            } else {
                datos[7] = "Inactivo";
            }
            prcs_repetidos.llenarFilas(datos, tamaño, Tbl_Datos);
        }
    }

